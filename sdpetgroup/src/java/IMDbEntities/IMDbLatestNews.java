package IMDbEntities;

/**
 *
 * @author kendrabooker
 */
public class IMDbLatestNews {
    
    private String image, header, article;
    
    public IMDbLatestNews() {}

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getHeader() {
        return header;
    }

    public void setHeader(String header) {
        this.header = header;
    }

    public String getArticle() {
        return article;
    }

    public void setArticle(String article) {
        this.article = article;
    }

    
    
    
}
